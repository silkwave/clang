#include <stdio.h>
#include "util.h"

int main()
{
    // 1. 트림 함수 테스트 (MAX_SIZE 10자리 규격에 부합하는 샘플 데이터 배치)
    char amt1[MAX_SIZE + 1] = "0000123456";
    int rcLen;

    printf("=== 1. 트림 함수 테스트 ===\n");
    // sizeof(amt1)은 11이 들어가며, 함수 내부 가드에 의해 10자리로 제어됩니다.
    rcLen = cdcd_lspace_zero_amt_trim(amt1, sizeof(amt1) - 1);
    printf("트림 결과: \"%s\" (길이: %d)\n\n", amt1, rcLen);

    // 2. 정수 변환 함수 테스트 (int 리턴값 활용)
    printf("=== 2. 정수 변환 함수 테스트 (Return 방식) ===\n");

    char numStr1[] = "00123456";
    int result1;

    // 함수의 return 값을 int 변수에 대입 (int 범위 내 안전 변환)
    result1 = cdcd_mk_int(numStr1, 8);
    printf("원본: \"%s\" (8자리 변환) -> 결과 정수: %d\n", numStr1, result1);

    char amt_buf[32];
    int fmt_len;

    printf("==================================================\n");
    printf("     cdcd_make_amt 실무 시나리오별 테스트         \n");
    printf("==================================================\n\n");

    // ------------------------------------------------------------------------
    // 케이스 1. 일반적인 우측 정렬 (양수 앞에 공백 채우기)
    // 용도: 대외 기관 전송 전문 규격 맞춤 (총 12자리 공간 필요)
    // ------------------------------------------------------------------------
    printf("[CASE 1] 일반 양수 + 우측 정렬 공백 패딩\n");

    strcpy(amt_buf, "1234567");
    fmt_len = cdcd_make_amt(amt_buf, 12, ' ');

    printf("  - 결과 데이터 : \"%s\"\n", amt_buf); // "   1,234,567" (공백 3칸 + 9글자)
    printf("  - 결과 길이   : %d\n\n", fmt_len);

    // ------------------------------------------------------------------------
    // 케이스 2. 명시적 양수 부호 표시 (양수 앞에 '+' 채우기)
    // 용도: 거래 명세서나 전일 대비 증감(+/-)을 화면에 강조할 때
    // ------------------------------------------------------------------------
    printf("[CASE 2] 명시적 양수 부호(+) 표시\n");

    strcpy(amt_buf, "5432100");
    fmt_len = cdcd_make_amt(amt_buf, 12, '+');

    printf("  - 결과 데이터 : \"%s\"\n", amt_buf); // "  +5,432,100" (공백 2칸 + '+' + 9글자)
    printf("  - 결과 길이   : %d\n\n", fmt_len);

    // ------------------------------------------------------------------------
    // 케이스 3. 음수 금액 처리 (자동 '-' 부호 배치)
    // 특이사항: 음수가 들어오면 세 번째 인자(edt_chr)는 무시되고 '-'가 붙습니다.
    // ------------------------------------------------------------------------
    printf("[CASE 3] 음수 금액 처리 (자동 '-' 부호 전환)\n");

    strcpy(amt_buf, "-9876543");
    fmt_len = cdcd_make_amt(amt_buf, 15, '+'); // '+'를 주어도 음수이므로 '-'가 우선됨

    printf("  - 결과 데이터 : \"%s\"\n", amt_buf); // "     -9,876,543" (공백 5칸 + 10글자)
    printf("  - 결과 길이   : %d\n\n", fmt_len);

    // ------------------------------------------------------------------------
    // 케이스 4. 한 자릿수 또는 '0'원 처리
    // 용도: 잔액이 없거나 단돈 몇 원만 남았을 때 포맷 붕괴 방지
    // ------------------------------------------------------------------------
    printf("[CASE 4] '0'원 및 한 자릿수 금액 처리\n");

    // 0원 테스트
    strcpy(amt_buf, "0");
    cdcd_make_amt(amt_buf, 10, ' ');
    printf("  - 0원 가공    : \"%s\"\n", amt_buf); // "         0" (공백 9칸 + '0')

    // 단돈 7원 테스트
    strcpy(amt_buf, "7");
    cdcd_make_amt(amt_buf, 10, ' ');
    printf("  - 7원 가공    : \"%s\"\n\n", amt_buf); // "         7" (공백 9칸 + '7')

    // ------------------------------------------------------------------------
    // 케이스 5. 데이터 유실 방어막 작동 (길이 초과 에러)
    // 상황: 허용된 출력 길이(10자리)보다 콤마 포함 데이터(13자)가 더 길 때
    // 결과: 금융 사고 예방을 위해 전체 버퍼를 에러 표시('*')로 밀어버림
    // ------------------------------------------------------------------------
    printf("[CASE 5] 방어 코드 작동 (지정 자릿수 초과 시 에러 처리)\n");

    strcpy(amt_buf, "1234567890");             // 10자리 숫자 -> 콤마 추가 시 "1,234,567,890" (13자 필요)
    fmt_len = cdcd_make_amt(amt_buf, 10, ' '); // 하지만 출력을 10자리로 제한 요청함

    printf("  - 결과 데이터 : \"%s\"\n", amt_buf); // "**********" (데이터 오염 전 차단)
    printf("  - 결과 길이   : %d\n", fmt_len);
    printf("==================================================\n");

    return 0;
}